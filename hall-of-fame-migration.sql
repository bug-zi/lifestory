-- 2. 创建探索记录表（每个用户对每个人物的探索状态）
CREATE TABLE IF NOT EXISTS hall_of_fame_explorations (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  person_id integer REFERENCES hall_of_fame(id) ON DELETE CASCADE NOT NULL,
  explored boolean DEFAULT false,
  script_id uuid REFERENCES scripts(id) ON DELETE SET NULL,
  created_at timestamptz DEFAULT now(),
  UNIQUE(user_id, person_id)
);

-- 3. 启用 RLS
ALTER TABLE hall_of_fame_explorations ENABLE ROW LEVEL SECURITY;

-- 4. RLS 策略
