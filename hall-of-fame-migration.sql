  CREATE POLICY "Users can view own explorations" ON hall_of_fame_explorations
    FOR SELECT USING (auth.uid() = user_id);
  EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

DO $$ BEGIN
  CREATE POLICY "Users can insert own explorations" ON hall_of_fame_explorations
    FOR INSERT WITH CHECK (auth.uid() = user_id);
